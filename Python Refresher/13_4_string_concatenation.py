'''
String Concatenation in Python

To concatenate, or combine, two strings you can use the + operator.
'''
print("Merge variable a with variable b into variable c:")
a = "Hello"
b = "World"
c = a + b
print(c)

print('\nExample 2--To add a space between them, add a " ":')
a = "Hello"
b = "World"
c = a + " " + b
print(c)