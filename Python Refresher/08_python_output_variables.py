'''
Output Variables

Commas support different variable types
For "+" it does not
'''

# In the print() function, you output multiple variables, separated by a comma:
print("----Comma----")

x = "Python"
y = "is"
z = "awesome"
print(x, y, z)

# for commas, you can also do:
print("---Comma Ex2---\n")
x = 5
y = "John"
print(x, y)

# You can also use the + operator to output multiple variables:
print("---- + -----\n")

x = "Python "
y = "is "
z = "awesome"
print(x + y + z)


